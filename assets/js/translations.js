// Language management
class LanguageManager {
    constructor() {
        this.currentLanguage = this.getStoredLanguage() || 'it'; // Italian as default
        this.isTransitioning = false;
        this.init();
    }
    
    init() {
        this.createLanguageSwitcher();
        this.translatePage();
        this.setupLanguageSwitcher();
        this.updateDynamicDate();
    }
    
    getStoredLanguage() {
        return localStorage.getItem('oceanTableLanguage');
    }
    
    setLanguage(lang) {
        if (translations[lang] && !this.isTransitioning) {
            this.isTransitioning = true;
            
            // Add transition class to body
            document.body.classList.add('language-transitioning');
            
            // Show loading indicator
            this.showLanguageLoader();
            
            setTimeout(() => {
                this.currentLanguage = lang;
                localStorage.setItem('oceanTableLanguage', lang);
                this.updateLanguageSwitcher();
                this.translatePage();
                this.updateDynamicDate();
                this.updatePageLanguage();
                
                // Hide loading indicator
                this.hideLanguageLoader();
                
                // Remove transition class
                setTimeout(() => {
                    document.body.classList.remove('language-transitioning');
                    this.isTransitioning = false;
                }, 300);
            }, 150);
        }
    }
    
    createLanguageSwitcher() {
        const languageElements = document.querySelectorAll('.language-switch');
        languageElements.forEach(element => {
            element.innerHTML = `
                <div class="language-toggle">
                    <div class="language-option" data-lang="it">IT</div>
                    <div class="language-option" data-lang="en">EN</div>
                    <div class="language-slider ${this.currentLanguage === 'en' ? 'slide-right' : ''}"></div>
                </div>
            `;
        });
    }
    
    updateLanguageSwitcher() {
        const sliders = document.querySelectorAll('.language-slider');
        const options = document.querySelectorAll('.language-option');
        
        sliders.forEach(slider => {
            if (this.currentLanguage === 'en') {
                slider.classList.add('slide-right');
            } else {
                slider.classList.remove('slide-right');
            }
        });
        
        options.forEach(option => {
            if (option.getAttribute('data-lang') === this.currentLanguage) {
                option.classList.add('active');
            } else {
                option.classList.remove('active');
            }
        });
    }
    
    translatePage() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.getTranslation(key);
            if (translation) {
                // Add fade effect during translation
                element.style.opacity = '0.7';
                
                setTimeout(() => {
                    if (element.tagName === 'INPUT' && element.type === 'submit') {
                        element.value = translation;
                    } else if (element.hasAttribute('placeholder')) {
                        element.placeholder = translation;
                    } else {
                        element.textContent = translation;
                    }
                    
                    element.style.opacity = '1';
                }, 100);
            }
        });
        
        // Update alt texts for images
        const images = document.querySelectorAll('img[data-translate-alt]');
        images.forEach(img => {
            const key = img.getAttribute('data-translate-alt');
            const translation = this.getTranslation(key);
            if (translation) {
                img.alt = translation;
            }
        });
    }
    
    getTranslation(key) {
        return translations[this.currentLanguage][key] || translations['it'][key] || key;
    }
    
    setupLanguageSwitcher() {
        const languageOptions = document.querySelectorAll('.language-option');
        languageOptions.forEach(option => {
            // Add keyboard accessibility
            option.setAttribute('tabindex', '0');
            option.setAttribute('role', 'button');
            option.setAttribute('aria-label', `Switch to ${option.getAttribute('data-lang') === 'it' ? 'Italian' : 'English'}`);
            
            // Click handler
            option.addEventListener('click', (e) => {
                e.stopPropagation();
                const lang = option.getAttribute('data-lang');
                if (lang !== this.currentLanguage) {
                    this.setLanguage(lang);
                }
            });
            
            // Keyboard handler
            option.addEventListener('keydown', (e) => {
                if (e.key === 'Enter' || e.key === ' ') {
                    e.preventDefault();
                    e.stopPropagation();
                    const lang = option.getAttribute('data-lang');
                    if (lang !== this.currentLanguage) {
                        this.setLanguage(lang);
                    }
                }
            });
            
            // Focus styles
            option.addEventListener('focus', () => {
                option.style.outline = '2px solid var(--primary-blue)';
                option.style.outlineOffset = '2px';
            });
            
            option.addEventListener('blur', () => {
                option.style.outline = 'none';
            });
        });
    }
    
    updateDynamicDate() {
        const currentYear = new Date().getFullYear();
        const copyrightElements = document.querySelectorAll('.dynamic-copyright');
        
        copyrightElements.forEach(element => {
            const copyrightText = this.getTranslation('footer.copyright');
            const brandName = this.currentLanguage === 'it' ? 'Tavolo dell\'Oceano' : 'Ocean Table';
            element.innerHTML = `&copy; ${currentYear} ${brandName}. ${copyrightText}`;
        });
    }
    
    updatePageLanguage() {
        document.documentElement.lang = this.currentLanguage;
        
        // Update page title based on current page
        const currentPage = window.location.pathname;
        if (currentPage.includes('menu')) {
            document.title = this.currentLanguage === 'it' ? 'Menu - Tavolo dell\'Oceano' : 'Menu - Ocean Table';
        } else if (currentPage.includes('careers')) {
            document.title = this.currentLanguage === 'it' ? 'Carriere - Tavolo dell\'Oceano' : 'Careers - Ocean Table';
        } else if (currentPage.includes('gift-card')) {
            document.title = this.currentLanguage === 'it' ? 'Carte Regalo - Tavolo dell\'Oceano' : 'Gift Cards - Ocean Table';
        } else {
            document.title = this.currentLanguage === 'it' ? 'Tavolo dell\'Oceano - Ristorante di Pesce Premium' : 'Ocean Table - Premium Seafood Restaurant';
        }
    }
    
    showLanguageLoader() {
        // Create or show language loading indicator
        let loader = document.querySelector('.language-loader');
        if (!loader) {
            loader = document.createElement('div');
            loader.className = 'language-loader';
            loader.innerHTML = `
                <div class="loader-content">
                    <div class="loader-spinner"></div>
                    <span class="loader-text">${this.getTranslation('language.changing')}</span>
                </div>
            `;
            document.body.appendChild(loader);
        }
        loader.style.display = 'flex';
    }
    
    hideLanguageLoader() {
        const loader = document.querySelector('.language-loader');
        if (loader) {
            loader.style.display = 'none';
        }
    }
}

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    window.languageManager = new LanguageManager();
});

// Export for use in other files
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { translations, LanguageManager };
}